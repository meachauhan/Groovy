class Operators{
    static main(args) {
        // AssignmentOperator
        println("Addition= "+ 4+5)
        println("Subtraction= "+5-2)
        println("Multiplication= "+7*3)
        println("Division= "+5/2)


        //Range Operator
        def range = 5..10; 
        println(range); 
        println(range.get(2)); 
    }
}